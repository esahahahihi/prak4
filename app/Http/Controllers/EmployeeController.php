    public function store(Request $request)
    {
        $messages = [
        'required' => 'Tulung Diisi Rekkk',
        'email' => 'Diisi disek karo email sing bener',
        'numeric' => 'Diisi karo nomer lurrr'
        ];
        $validator = Validator::make($request->all(), [
        'firstName' => 'required',
        'lastName' => 'required',
        'email' => 'required|email',
        'age' => 'required|numeric',
        ], $messages);
        if ($validator->fails()) {
            return redirect()->back()->withErrors($validator)->withInput();
        }
        return $request->all();
    }

